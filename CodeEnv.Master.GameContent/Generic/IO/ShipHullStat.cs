    public class ShipHullStat : AHullStat {

        public override Vector3 HullDimensions { get { return HullCategory.Dimensions(); } }

        public ShipHullCategory HullCategory { get; private set; }

        /// <summary>
        /// The constraint on the ability of the engine(s) to turn this hull. 
        /// <remarks>Expressed as a percentage of the maximum turn rate the ship's engines are capable of. Hull's TurnRateConstraint * Engine's MaxTurnRate = ship's turn rate.</remarks>
        /// </summary>
        public float TurnRateConstraint { get; private set; }

        /// <summary>
        /// The drag of this hull in Topography.OpenSpace.
        /// </summary>
        public float Drag { get; private set; }
        public float Income { get; private set; }
        public float Science { get; private set; }
        public float Culture { get; private set; }

