        /// <param name="expense">The expense.</param>
        /// <param name="dmgMitigation">The DMG mitigation.</param>
        /// <param name="hqPriority">The HQ priority.</param>
        /// <param name="turnRateConstraint">The constraint on the ability of the engine(s) to turn this hull.</param>
        /// <param name="science">The science generated by this hull, if any.</param>
        /// <param name="culture">The culture generated by this hull, if any.</param>
        /// <param name="income">The income generated by this hull, if any.</param>
        public ShipHullStat(string name, AtlasID imageAtlasID, string imageFilename, string description, EquipmentStatID id, float size, float mass,
            float pwrRqmt, float hitPts, float constructionCost, float expense, DamageStrength dmgMitigation, Priority hqPriority,
            float turnRateConstraint, float science, float culture, float income)
            : base(name, imageAtlasID, imageFilename, description, id, size, mass, pwrRqmt, hitPts, constructionCost, expense, dmgMitigation,
                  hqPriority) {
            HullCategory = id.Category.ShipHullCat();
            Drag = HullCategory.Drag();
            TurnRateConstraint = turnRateConstraint;

            Science = science;
            Culture = culture;
            Income = income;
        }



